namespace DotTrend
{
    /// <summary>
    /// Aggregate functions supported by the trend library
    /// </summary>
    public enum AggregateFunction
    {
        /// <summary>
        /// Represents the count aggregate function.
        /// </summary>
        Count,

        /// <summary>
        /// Represents the sum aggregate function.
        /// </summary>
        Sum,

        /// <summary>
        /// Represents the average aggregate function.
        /// </summary>
        Average,

        /// <summary>
        /// Represents the minimum aggregate function.
        /// </summary>
        Min,

        /// <summary>
        /// Represents the maximum aggregate function.
        /// </summary>
        Max
    }
}